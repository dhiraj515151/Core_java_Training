
public class Coach {
	private String coachId;
	private String firstName;
	private String lastName;
	private String coachesFor;
	
	
	@Override
	public String toString() {
		return "Coach [coachId=" + coachId + ", firstName=" + firstName + ", lastName=" + lastName + ", coachesFor="
				+ coachesFor + "]";
	}
	

}
